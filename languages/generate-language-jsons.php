// ============================================================
// Generate JSON files
// ============================================================
foreach ($translations as $locale => $trans) {
    $file_path = $base_dir . '/' . $locale . '.json';
    
    // Skip if file already exists with more entries
    if (file_exists($file_path)) {
        $existing = json_decode(file_get_contents($file_path), true);
        if ($existing && count($existing) > count($trans)) {
            echo "Skipping $locale - existing file has more entries (" . count($existing) . " vs " . count($trans) . ")\n";
            continue;
        }
    }
    
    // Sort by key for consistency
    ksort($trans);
    
    $json = json_encode($trans, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE);
    file_put_contents($file_path, $json);
    echo "Generated $locale.json with " . count($trans) . " entries\n";
}

