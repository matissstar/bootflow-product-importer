// Read all msgids from .pot file
$pot_file = __DIR__ . '/bootflow-product-importer.pot';
$pot_content = file_get_contents($pot_file);
preg_match_all('/^msgid "(.*)"$/m', $pot_content, $matches);
$all_msgids = array_filter($matches[1], function($s) { return $s !== ''; });

// Also handle multi-line msgids
preg_match_all('/msgid\s+""\n((?:"[^"]*"\n)+)/m', $pot_content, $multi_matches);
foreach ($multi_matches[1] as $multi) {
    preg_match_all('/"([^"]*)"/', $multi, $parts);
    $full = implode('', $parts[1]);
    if ($full) $all_msgids[] = $full;
}

$all_msgids = array_unique($all_msgids);
echo "Found " . count($all_msgids) . " msgids\n";

