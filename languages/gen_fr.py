import json, os

TRANSLATIONS_DIR = "translations"
ALL_STRINGS = json.load(open("all_strings.json"))

def merge_and_save(locale, translations):
    path = os.path.join(TRANSLATIONS_DIR, f"{locale}.json")
    existing = json.load(open(path))
    added = 0
    for k, v in translations.items():
        if k not in existing:
            existing[k] = v
            added += 1
    with open(path, "w") as f:
        json.dump(existing, f, ensure_ascii=False, indent=2)
    covered = sum(1 for s in ALL_STRINGS if s in existing)
    print(f"  {locale}: +{added} new → {covered}/{len(ALL_STRINGS)} ({100*covered/len(ALL_STRINGS):.1f}%)")

